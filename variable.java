public class variable {
  public static void main(String[] args) {
    /*
     * 변수 ( Variable )
     * - 데이터를 저장하는 메모리 공간
     * - 미리 지정된 데이터타입의 데이터만 저장가능
     * - 변수를 사용하기 위해서는 반드시 먼저 변수 선언을 해야함
     * - 데이터를 저장하는 작업을 변수 초기화 ( Initialization ) 이라고 함
     */

    int a = 10;
    System.out.println("a : " + a);
    a = 20;
    System.out.println("a : " + a);

    // int b = "안녕"; type mismatch !
    // System.out.println(b);
  }
}
